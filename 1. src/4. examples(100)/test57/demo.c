// ===================== 系统头文件 ===============================
# include<stdio.h>
// ================================================================
// 函数名称：main
// 函数功能：按位取反
// 输入参数：无
// 输出参数：无
// 全局变量：无
// 调用模块：printf 
// ================================================================
int main(void)
{
	unsigned short u16InputA = 0x77;
	
	u16InputA = ~u16InputA;// 取反
	printf("0x%04x\n",u16InputA);// 打印信息

	return 0;
}
